# FunciÃ³n para obtener apuestas en tiempo real
async def obtener_apuestas():
    async with aiohttp.ClientSession() as session:
        async with session.get(URL) as response:
            if response.status == 200:
                datos = await response.json()
                print("ğŸ“Š Datos de apuestas recibidos:", datos)  # DepuraciÃ³n
                return datos
            else:
                print(f"âŒ Error en la API: {response.status}")
                return []

# FunciÃ³n para procesar y filtrar las mejores apuestas
async def procesar_apuestas():
    datos_apuestas = await obtener_apuestas()
    mejores_apuestas = []

    for evento in datos_apuestas:
        partido = f"{evento['home_team']} vs {evento['away_team']}"
        mejores_cuotas = []

        for book in evento.get("bookmakers", []):
            for market in book.get("markets", []):
                if market["key"] == "h2h":  # Apuestas 1X2 (Victoria/Empate/Derrota)
                    odds = market["outcomes"]
                    mejores_cuotas.append(
                        f"ğŸ† {book['title']}: {odds[0]['name']} ({odds[0]['price']}) / {odds[1]['name']} ({odds[1]['price']})"
                    )

        if mejores_cuotas:
            mensaje = f"âš½ {partido}\n" + "\n".join(mejores_cuotas)
            mejores_apuestas.append(mensaje)

    if mejores_apuestas:
        mensaje_final = "ğŸ”¥ Mejores apuestas disponibles:\n\n" + "\n\n".join(mejores_apuestas)
        await bot.send_message(chat_id=CHAT_ID, text=mensaje_final)
    else:
        print("âš ï¸ No se encontraron apuestas con alto valor.")

